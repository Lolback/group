package bean;

public class ClassNum {
	private int class_num;
	private int school_cd;
	//クラス番号と学校番号を取得する

	public int getClassNum() {
		return class_num;
	}

	public void setClassNum(int class_num) {
		this.class_num = class_num;
	}

	public int getSchoolCd() {
		return school_cd;
	}

	public void setSchoolCd(int school_cd) {
		this.school_cd = school_cd;
	}

}
